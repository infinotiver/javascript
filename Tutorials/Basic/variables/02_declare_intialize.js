/**
 * This code snippet demonstrates how to declare and initialize variables in JavaScript.
 * 
 * It shows how to declare a variable, assign a value to it using the assignment operator,
 * and assign the value of one variable to another.
 * 
 * Finally, it logs the value of the variable to the console.
 */

// Declare a variable called 'something'
var something; // Optional semicolon

// Assign a value of "someone" to the 'something' variable
something = "someone";

// Declare a variable called 'some_var_124' and assign it a value of 124
var some_var_124 = 124;

// Assign the value of 'some_var_124' to the 'something' variable
var something = some_var_124;

