/*
DECREMENTING

This code snippet demonstrates how to decrement a variable by one.
There are two ways to achieve this, the recommended way is using the decrement operator --.

*/

// Not recommended way

// Declare a variable and assign it a value
let some_number = 25;
// var some_number = 25 also works perfectly

// Subtract one from the existing value using the subtraction operator
some_number  = some_number - 1;

// Recommended way (quicker) 

// Decrement the value of the variable by one using the decrement operator
some_number --; 


