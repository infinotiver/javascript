/**
 * Incrementing a number by one.
 *
 * This code snippet demonstrates how to increment a variable by one.
 * It shows two ways to achieve this, the recommended way is using the increment operator ++.
 *
 * @example
 * let some_number = 25;
 * some_number++; // increments by one
 */

// Not recommended way

let some_number = 25; // define a variable and assign it a value

some_number = some_number + 1; // incrementing by one

// Recommended way (quicker)

some_number ++; // adds one

