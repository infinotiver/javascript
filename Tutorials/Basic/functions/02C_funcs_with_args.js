function ConcatenatedStrings(firstString, secondString) {
    // Concatenating the two arguments and logging the result
    console.log('Concatenated:', firstString + secondString);
}

ConcatenatedStrings('hello', 'world'); // Output: Concatenated: helloworld

function Multiply(argument1, argument2) {
    // Multiplying the two arguments and logging the result
    console.log('Multiplied:', argument1 * argument2);
}
Multiply(3, 4); // Output: Multiplied: 12