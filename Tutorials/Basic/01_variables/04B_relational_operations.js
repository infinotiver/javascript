/*
This file demonstrates the use of relational operators in JavaScript.
Relational operators are used to compare two values.
They return a boolean value (true or false) indicating the outcome of the comparison.
*/
// Comparison (or Relational) Operators
let num1 = 10;
let num2 = 15;
console.log("num1: ", num1, "num2: ", num2);
let isEqual = num1 == num2; // equal to
console.log("num1 == num2: ", isEqual);
let isNotEqual = num1 != num2; // not equal to
console.log("num1 != num2: ", isNotEqual);
let isGreater = num1 > num2; // greater than
console.log("num1 > num2: ", isGreater);
let isLess = num1 < num2; // less than
console.log("num1 < num2: ", isLess);
let isGreaterOrEqual = num1 >= num2; // greater than or equal to
console.log("num1 >= num2: ", isGreaterOrEqual);
let isLessOrEqual = num1 <= num2; // less than or equal to
console.log("num1 <= num2: ", isLessOrEqual);

